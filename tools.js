function openTool(type){
  if(!appData.pro) return showNotification("Pro required","error"), nav('billing');
  document.getElementById("toolModal").classList.add("active");
  let html = '';
  
  if(type=='resume') html = `<h2>Resume Maker</h2><input id="tName" placeholder="Full Name"><textarea id="tExp" placeholder="Experience"></textarea><button class="btn" onclick="genTool('resume')">Generate & Copy</button>`;
  if(type=='post') html = `<h2>Social Post Generator</h2><input id="tTopic" placeholder="Topic"><button class="btn" onclick="genTool('post')">Generate</button>`;
  if(type=='yt') html = `<h2>YT Script</h2><input id="tTitle" placeholder="Video Title"><button class="btn" onclick="genTool('yt')">Generate</button>`;
  
  document.getElementById("toolContent").innerHTML = html + `<button class="btn-danger" onclick="document.getElementById('toolModal').classList.remove('active')" style="margin-top:10px">Close</button>`;
}

